    void editar_cliente(){
        DefaultTableModel modelo =(DefaultTableModel) tabla_cliente.getModel();
        for(int i = 0; i < tabla_cliente.getRowCount(); i++){
            Cliente cliente = new Cliente();
            String client = cliente.getNombre() + " "+ cliente.getDocumento() +" " + cliente.getDireccion() + " "+ cliente.getTelefono() + " "+ cliente.getEmail(); 
            modelo.add(i, client);
            i ++;
        }
        tabla_cliente.setModel(modelo);
    }
    
